package java1;

public class Concatination {
	public static void main(String[] args) {
		String name="Anand";
		String surname="Bhayre";
		int age=30;
		int bike=1;
		System.out.println(name+" "+surname);
		System.out.println(age+bike);
		System.out.println(name+" "+surname+age+bike);
		System.out.println(name+" "+surname+(age+bike));
	}

}

